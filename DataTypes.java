public class DataTypes {
    public static void main(String[]args){
        int num = 20;
        float fnum = 5.92f;
        char c = 'A';
        String name = "billu";
        final int num2 = 25;
        boolean bool = true;
        System.out.println("The value of Integer is " + num);
        System.out.println("The value of float is " + fnum);
        System.out.println("The value of char is " + c);
        System.out.println("The value of String is " + name);
        System.out.println("The value of final Integer is " + num2);
        System.out.println("The value of boolean is " + bool);
        int x = 25, y = 2562, z = 2152;
        System.out.println(x + y + z);

    
    }
}